CREATE TABLE FINKRATZEN.BOA_IMPORT(
  POSTING_DATE VARCHAR, 
  DESCRIPTION VARCHAR, 
  AMOUNT VARCHAR,
  MD5 VARCHAR
 )
AS SELECT
  DESCRIPTION, 
  COLUMN1, 
  "Summary Amt.",
  NULL 
FROM 
	CSVREAD('{{file-name}}')
WHERE 
	DESCRIPTION REGEXP '^[0-9/0-9/0-9]'
	AND "Summary Amt." IS NOT NULL;

